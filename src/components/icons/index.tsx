/* 动态生成命名导出
 * 用 Proxy 拦截 import 访问，未在 icons 中的名字返回 fallback
 */
export const _registry = new Proxy(icons, {
  get(target, prop: string) {
    if (prop in target) return target[prop];
    console.warn(`[icons] Icon "${prop}" not pre-registered, using runtime fallback`);
    return createNamedIcon(prop);
  },
});

