import * as Geist from "@geist-ui/icons";
import * as Phosphor from "@phosphor-icons/react";
import * as LucideAll from "lucide-react";
import type React from "react";
import { type CSSProperties, type ComponentType, type SVGProps, forwardRef } from "react";

/* ---------- 类型 ---------- */

export type IconSkin =
  | "default"
  | "linear"
  | "chatgpt"
  | "notion"
  | "vercel"
  | "grafana"
  | "github"
  | "admin"
  | "flywheel"
  | "analysis"
  | "evaluation";

export type IconWeight = "regular" | "bold" | "fill" | "duotone" | "light" | "thin";

export type IconProps = SVGProps<SVGSVGElement> & {
  /** 借皮场景，决定 weight */
  skin?: IconSkin;
  /** 图标 weight（覆盖 skin 默认） */
  weight?: IconWeight;
  /** 像素尺寸（默认 16） */
  size?: number;
  /** 强制从指定库取 */
  source?: "phosphor" | "geist" | "lucide" | "auto";
};

/** 通用图标组件类型（业务方用，替代 lucide 的 LucideIcon） */
export type IconComponent = ComponentType<{
  className?: string;
  size?: number;
  style?: CSSProperties;
  ref?: React.Ref<SVGSVGElement>;
}>;

const SKIN_TO_WEIGHT: Record<IconSkin, IconWeight> = {
  default: "regular",
  linear: "regular",
  chatgpt: "bold",
  notion: "light",
  vercel: "regular",
  grafana: "fill",
  github: "regular",
  admin: "regular",
  flywheel: "regular",
  analysis: "regular",
  evaluation: "regular",
};

function toPascal(input: string): string {
  return input
    .replace(/[-_\s]+(.)?/g, (_m, c: string | undefined) => (c ? c.toUpperCase() : ""))
    .replace(/^(.)/, (m) => m.toUpperCase());
}

type AnyIcon = ComponentType<Record<string, unknown>>;
const _Phosphor = Phosphor as unknown as Record<string, AnyIcon>;
const _Geist = Geist as unknown as Record<string, AnyIcon>;
const _Lucide = LucideAll as unknown as Record<string, AnyIcon>;

function resolveIcon(
  name: string,
  weight: IconWeight,
  source: "phosphor" | "geist" | "lucide" | "auto",
  size: number,
  className: string | undefined,
  style: CSSProperties | undefined,
  extraProps: Record<string, unknown> = {},
): React.ReactNode {
  const passProps = { size, weight, className, style, ...extraProps } as Record<string, unknown>;

  if (source === "phosphor" || source === "auto") {
    const P = _Phosphor[toPascal(name)] ?? _Phosphor[name];
    if (P) return <P {...passProps} />;
  }

  if (source === "geist" || source === "auto") {
    const G = _Geist[toPascal(name)] ?? _Geist[name];
    if (G) return <G size={size} className={className} style={style} {...extraProps} />;
  }

  const L = _Lucide[toPascal(name)] ?? _Lucide[name];
  if (L) {
    return (
      <L className={className} style={{ width: size, height: size, ...style }} {...extraProps} />
    );
  }

  const F = _Lucide.Activity;
  return (
    <F className={className} style={{ width: size, height: size, ...style }} {...extraProps} />
  );
}

export function Icon({
  name,
  skin = "default",
  weight: weightProp,
  size = 16,
  source = "auto",
  className,
  style,
}: IconProps & { name: string }) {
  const weight = weightProp ?? SKIN_TO_WEIGHT[skin];
  return resolveIcon(name, weight, source, size, className, style);
}

/* ---------- 命名导出 ---------- */

type NamedIcon = ComponentType<
  Partial<Omit<IconProps, "name">> & { ref?: React.Ref<SVGSVGElement> }
>;

function createNamedIcon(name: string): NamedIcon {
  const Comp = forwardRef<SVGSVGElement, Partial<Omit<IconProps, "name">>>(
    function IconImpl(props, ref) {
      const {
        skin = "default",
        weight: weightProp,
        size = 16,
        source = "auto",
        className,
        style,
        ...rest
      } = props;
      const weight = weightProp ?? SKIN_TO_WEIGHT[skin];
      return resolveIcon(name, weight, source, size, className, style, {
        ...(rest as Record<string, unknown>),
        ref,
      });
    },
  );
  Comp.displayName = `Icon${toPascal(name)}`;
  return Comp as unknown as NamedIcon;
}

