/**
 * 图标系统（v8 Lego · 借皮图标）
 *
 * 设计原则：
 *   1. 业务方只 import 一个图标名（如 <IconHome />），不直接接触底层库
 *   2. 根据"借皮场景"自动选用对应图标库的对应 weight
 *   3. 缺图时 fallback 到 lucide-react
 *   4. size 默认 16，可由父级 className 覆盖
 *
 * 用法：
 *   import { IconHome } from "@/components/icons";
 *   <IconHome className="h-4 w-4" />
 *   <IconHome skin="chatgpt" />  // 强制借皮
 *   <IconHome weight="duotone" /> // 强制 weight
 */

